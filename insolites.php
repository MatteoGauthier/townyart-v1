    <h3>La fresque géante du CHU Pellegrin </h3> <br> <br>
        <p>C’est en allant prendre mon repas au restaurant universitaire que je passe pour la première fois devant cet hôpital. Aux premiers abords, rien d'étonnant, jusqu’à ce que je me tourne vers la gauche. Là, j'aperçois une petite fille qui me fixe du regard. Une petite fille… de 15 mètres de hauteur. On est d'accord, c’est impossible… Mais tu vas bientôt comprendre pourquoi ça l'est. <br> <br>
    Remontons dans le temps jusqu’en 2013, année où l’artiste pochoiriste <a class="lien" href="https://www.jefaerosol.com"> Jef Aerosol </a> est appelé pour la réalisation d’une œuvre de 15m x 6m à l’entrée de l'<a class="lien" href="https://fr.wikipedia.org/wiki/Hôpital_Pellegrin">hôpital Pellegrin</a>. Cette œuvre, réalisée en 4 jours, représente une image forte et symbolique. En effet, cette fresque monumentale permet de casser l’image anxiogène qui règne à l’hôpital. <br> <br>
    <a class="lien" href="https://www.chu-bordeaux.fr/Espace-média/Archives/Philippe-Vigouroux-nommé-directeur-général-du-CHU-de-Bordeaux/">Philippe Vigouroux</a>, directeur général du CHU de Bordeaux en parle : <br>
    “La petite fille tient avec précaution un globe terrestre, porteur de toute une humanité. Son sourire est celui de l’hospitalité, sa gravité est celle de la responsabilité de qui tient des vies entre ses mains. » <br> <br>
    Mais pourquoi une petite fille ? L’artiste nous confie que l'enfance, c'est l'innocence, l'avenir, l'espoir. La jeunesse, c'est à la fois le futur et aussi ce que nous laissons derrière nous. A travers cette fresque, cette petite fille nous dit que ce monde nous appartient et c’est à nous de savoir ce que nous voulons en faire. <br> <br>
    On te conseille vivement d’aller à l'hôpital Pellegrin… Non pas pour des soucis de santé on l’espère, mais pour admirer ce chef d'œuvre visible <a class="lien" href="https://www.google.fr/maps/place/Groupe+Hospitalier+Pellegrin/@44.827611,-0.6087224,16z/data=!4m8!1m2!2m1!1spellegrin!3m4!1s0xd54d875aca3b753:0x57ed7f829e557bc8!8m2!3d44.827611!4d-0.604345"> Place Amélie Raba Léon à Bordeaux <br> <br> </p>
