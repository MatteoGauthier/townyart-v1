    <h3>La sculpture “Un détail”</h3> <br> <br>
    <p>Alors que nous nous promenons sur les quais de la rive gauche, non loin de la gare Saint Jean, notre œil ne peut qu'être attiré par cette immense structure à l'architecture futuriste. Mais attention ! Si tu veux en voir la totalité, il te faudra lever la tête. <br> <br>
    Siégeant sur la rive gauche de la Garonne et située à cinq minutes à pied de la gare Saint-Jean, <a class="lien" href="https://www.la-meca.com"> La MÉCA </a> est une arche en béton brute mesurant 37 mètres de haut et 180 mètres de large. <br> <br>
    Conçu par l'architecte <a class="lien" href="https://fr.wikipedia.org/wiki/Bjarke_Ingels"> BIG </a> (Bjarke Ingels Group) et conçu en septembre 2018, ce bâtiment est caractérisé par ses formes futuristes et ses dimensions gigantesques, comme si cette arche venait tout droit du futur. <br> <br>
    Derrière cet édifice se cache une statue étrangement perturbante reposant sur les gradins extérieurs. Cette œuvre, qui s’intitule “<a class="lien" href="http://www.aquitaineonline.com/actualites-en-aquitaine/sud-ouest/7222-sculpture-un-detail-de-benoit-maire.html">Un détail</a>” est celle de <a class="lien" href="https://www.franceculture.fr/personne-benoit-maire"> Benoît Maire </a>, artiste Pessacais de 43 ans. <br> <br>
    Faite de bronze, elle représente une demi-tête d'Hermès. Cette sculpture de 3 mètres de hauteur, à été réalisée par l’atelier de fonderie artisanale “Fonderie des cyclopes” à Mérignac. <br> <br>
    Selon l'artiste : « La moitié absente de la sculpture est à compléter par le spectateur dans l'intérieur vidé de la chambre urbaine. Ce travail de l'imagination est renforcé par le fini poli-miroir de la tranche coupée qui permet de faire l'expérience de la réflexion dans son sens littéral. » <br> <br>
    Une œuvre qui vaut le détour si tu aimes les illusions d’optiques et la sculpture, mais aussi la démesure. La MÉCA est situé au <a class="lien" href="https://www.google.fr/maps/place/La+Méca/@44.8283478,-0.5529029,17.95z/data=!4m8!1m2!2m1!1zbcOpY2E!3m4!1s0xd552647aaf3f8c9:0xa1e23388c8395821!8m2!3d44.828803!4d-0.5525432"> PARVIS CORTO MALTESE, Quai de Paludate, 33800 Bordeaux. </a> </p>
